    private void postCommentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_postCommentActionPerformed
        
        String writtenComment = writecomment.getText();
        if (!writtenComment.equals(""))
        {
            handler.insertComment(Login.getUserId(), postId, writtenComment);
            showComments();
        }
        else 
        {
            JOptionPane.showMessageDialog(null, "Empty Comment Not Allowed!"); 
        }

    }//GEN-LAST:event_postCommentActionPerformed

