    public Comment(int postId) throws ClassNotFoundException, SQLException
    {
        initComponents();
        setResizable(false);
        handler = DatabaseHandler.getInstance();
        
        this.postId = postId;
        
        ResultSet rs = handler.getPost(postId);
        
        username.setText("@" + rs.getString("username"));
        post.setText(rs.getString("post_text"));
        
        //commentList.setText("Hellos \n Yolo");
        showComments();
    }
    
    public void showComments() 
    {
        ResultSet rs = handler.getComments(postId);
        String s = "";
        try
        {
            do
            {
                s = s + rs.getString("comment_text") + "  @" + rs.getString("username") + "\n\n";
            }
            while (rs.next());
        }
        catch (Exception e)
        {
            System.out.println("Comment display error");
        }
        
        commentList.setText(s);
    }
    
    
    

